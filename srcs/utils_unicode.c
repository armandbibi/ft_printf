#include "../includes/ft_printf.h"

int wchartochar(int c, char *buffer)
{
	char str[5];

	if (c > 4194304 / 2)
	{
		ft_printf("yeah 1\n");
		str[3] = 0b10000000 | (c << 0);
		str[2] &= ~0b01000000;
		str[2] = 0b10000000 | (c << 7);
		str[2] &= ~0b01000000;
		str[1] = 0b10000000 | (c << 14);
		str[2] &= ~0b01000000;
		str[0] =   0b11110000 | (c << 21);
		str[2] &= ~0b00001000;
	}
	else if (c > 2048 / 2)
	{
		ft_printf("yeah 2\n");
		str[2] =   0b10000000 | (c);
		str[2] &= ~0b01000000;
		str[1] =   0b10000000 | (c >> 6);
		str[1] &=  ~0b01000000;
		str[0] =   0b11100000 | (c >> 12);
		write (1, str, 4);
		int count = 0;
		while (count++ < 5)
			printf("%#x \n", 0b10000000 - str[count]);
	}
	else if (c > 127)
	{
		ft_printf("yeah 3\n");
		str[1] =   0b10000000 | (c >> 0);
		str[2] &= ~0b01000000;
		str[0] =   0b11000000 | (c >> 6);
		str[2] &= ~0b00100000;
	}
	else
		str[0] = 0 | c;	
}
